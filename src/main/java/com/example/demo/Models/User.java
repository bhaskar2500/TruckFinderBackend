package com.example.demo.Models;

public class User {
        String email ; 
        String name;

        public void setEmail(String email) {
                this.email = email;
        }
        public void setName(String name) {
                this.name = name;
        }
        public String getEmail() {
                return email;
        }
        public String getName() {
                return name;
        }
}
